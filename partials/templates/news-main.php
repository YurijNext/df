						«Искусственный интеллект попытается улучшить свое окружение, не только ради выживания, но и с целью быть самодостаточным, составляя основу цивилизации, граничащей на пересечении природы и технологий», - пояснила Noumena. «Эти новые роботизированные организмы начнут манипулировать средой, обрабатывать ресурсы и формировать новые среды обитания посредством симбиотических ассоциаций среди элементов в ней».<br>
						Другими словами, роботы AI создали, так сказать, собственное общество и развились так, что каждый тип робота выполняет конкретную задачу. Все они работают вместе в гибридном природно-технологическом мире, чтобы выжить вместе с людьми.<br>
