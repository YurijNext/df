						<h3 class="title">3D-печать и нейронаука</h3>

						<p class="text">3D-печать, безусловно, является развивающейся новаторской технологией сама по себе, но интересным является то, как ее можно использовать для инноваций в других технологических областях, путем стимулирования новых исследований или непосредственного внедрения в практические исследования. Продолжая эту тенденцию, группа ученых в Великобритании недавно напечатала прототип шлема, который теперь будет использоваться на испытуемых, в их исследовательском проекте. Они изучают новую форму техники визуализации мозга, известную как магнитная энцефалография - Magnetoencephalography (MEG).<br>
						Исследование финансируется Британским биомедицинским исследовательским фондом «Wellcome», который предоставил научной команде 1,6 миллионов фунтов стерлингов для разработки нового типа сканера MEG, который потенциально может быть в четыре раза чувствительнее используемых в настоящее время устройств. MEG - это метод, который создает карты активности мозга путем измерения магнитных полей, генерируемых естественными электрическими токами в головном мозге. Ведущими исследования являются доктор Мэтью Брукс и профессор Ричард Боутелл из школы физики и астрономии Университета Ноттингема. Они работают в сотрудничестве с учеными из их школы, а также из университетского колледжа Лондона. Проект, как прогнозируется, продлится еще три года.<br>
						Исследования начались два года назад, сразу после того, как команда оценила потенциал квантовых датчиков для повышения чувствительности при сканировании MEG. Вклад «Wellcome» был произведён после завершения этого экспериментального этапа, и теперь учёные могут приступить к работе над созданием полностью функциональной системы MEG. £ 800 000 от общего финансирования будут направлены в Ноттингем, где будет проведено создание физической составной, необходимой для работы сканера. Дизайн и печать прототипа шлема были одними из первых этапов этого процесса, который все еще находится на ранних стадиях. Между тем исследователи UCL будут использовать оставшуюся часть гранта для проведения детального вычислительного и теоретического моделирования мозга, дополняя эту сферу новыми данными и ответами на вопросы, на которые ранее нейронаука не могла ответить.<br>
						По словам доктора Брукса, «квантовая технология позволила разработать новый тип оптического датчика, который обладает чувствительностью для обнаружения слабых магнитных полей мозга. В отличие от современной технологии, эти новые датчики могут работать при комнатной температуре, поэтому их можно разместить прямо на поверхности головы. Наши расчеты показывают, что, приближая датчики к головке, мы можем в четыре раза повысить чувствительность обнаружения поля. Это революционизирует тот эффект, которого мы добились при детекции сигналов из человеческого мозга».<br>
						В отличие от статических, одноразовых систем, которые ранее использовались, новая система MEG должна быть очень гибкой и адаптируемой. Это означает, что подгонка будет более комфортной, и пациенты смогут выполнять задания и свободно перемещаться в естественной среде. Это значительно расширит круг возможных вопросов и тем исследования. Новая система MEG также будет особенно полезна для экспериментов с детьми, поскольку способ работы предыдущих систем означал, что чувствительность была ограничена для субъектов с меньшими головами. «Квантовые датчики комнатной температуры могут быть установлены непосредственно на скальпе любого объекта», объясняет профессор Боутелл. «Это даст нам прогнозируемое четырехкратное увеличение чувствительности для взрослых, но чувствительность потенциально может увеличиться до 15 или 20 раз для детей или младенцев».
