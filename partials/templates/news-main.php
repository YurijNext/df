						<h3 class="title">Время, как инструмент 3D-печати, или же начало новой эры 4D</h3>

						<p class="text">Исследователи из известного Швейцарского федерального технологического института в Цюрихе (ETH Zurich) открывают новые горизонты в быстро расширяющейся области 4D-печати. Как было показано в новом исследовании профессоров механической и технологической инженерии Мариуса Вагнера, Тянь Чена и Кристины Ши, полимеры с памятью формы оказались важным катализатором при создании 4D-печатных изданий с улучшенной адаптируемостью и контролем. Согласно публикации авторов в научном журнале «3D Printing and Additive Manufacturing», использование этих специализированных полимеров позволяет печатать с предварительно определенными изменениями площади до 200%. Другими словами, они могут расширяться в два раза по сравнению с их первоначальным размером, открывая бесконечные возможности для производства будущего.<br>
						Это был долгий период для сторонников 4D в мире, а именно сам процесс эволюции 3D-печати во что-то большее, конечной характеристикой которого явилось время как 4-ое измерение. Эта, буквально, расширяющаяся технология - одна из самых ярких инноваций, возникающих в лабораториях по всему миру, позволяет ученым создавать трехмерные объекты печати, которые изменяются или самособираются с течением времени в заранее определяемые структуры. Хотя это технология,  по-прежнему на этапе пристальных исследований и разработок, будущее её выглядит ярким для 4D-печати. Недавно данный проект, получил статус megatrend в «Gartner Hype Cycle for Emerging Technologies 2017» и будет наблюдаться в нём в течение следующих пяти лет.<br>
						Теперь исследователи ETH Zurich быстро работают над тем, чтобы это будущее стало реальностью. Как пояснила в своей статье «Large Shape Transforming 4D Auxetic Structures», швейцарская инженерная команда, что помимо адаптации и реверсии 4D-печатные структуры могут демонстрировать изменения в области до двухсот процентов. Оказывается, ключ к руководству этими сложными геометрическими изменениями заключается в активации теплом. 3D-печатью в ауксетических метаматериалах, а именно в полимерах формы с термовязкоупругими свойствами материала, время может быть использовано в качестве четвертого измерения – когда и как нам заблагорассудится.<br>
						«4D-печать имеет большой неиспользованный потенциал в приложениях, где изменение конфигурации невозможно вручную и где электромеханическое приведение в действие невозможно, например, в аэрокосмической и медицинской областях», объясняют исследователи ETH Zurich. «Кроме того, 4D-конструкции имеют преимущество в отношении объема и поддержки».<br>
						Экспериментальная деятельность всегда была на первом плане разработки 4D-печати, но на этот раз команда ETH Zurich уделяла пристальное внимание задачам своих предшественников. Изучив обширный ряд проектов других исследователей, команда предпочла внедрить материал визуально идентичный сетке черепицы и способный формировать активные структуры различной сложности. Затем они смогли более пристально сосредоточиться на программной части своего дизайна, с новым упором на упрощенные процедуры.<br>
