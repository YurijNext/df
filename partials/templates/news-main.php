						<h3 class="title">Оптимизация производства или перенос запчастей в цифровой вид</h3>

						<p class="text">Технический исследовательский центр VTT в Финляндии и Университет Аалто опубликовали результаты двухлетнего отраслевого исследовательского проекта, исследующего, как компании могут использовать цифровые запасные части в своих интересах. Проект оценивается примерно в 1,4 миллиона евро (1,65 миллиона долларов США).<br>
						Еще в апреле 2016 года мы сообщили, что VTT Finland и Aalto University объединили свои усилия в двухлетнем проекте по исследованию использования оцифрованных запасных частей для дальнейшего производства посредством 3D-печати. Было сказано, что тринадцать компаний были вовлечены в массовое исследование, целью которого было бы дать четкие ответы о том, как предприятия могут эффективно использовать библиотеки цифровых запасных частей.<br>
						Теперь, когда этот двухлетний проект подошёл к завершению, финские учреждения обнародовали некоторые из своих выводов. И, как можно было ожидать, 3D-печать позиционируется с большим одобрением.<br>
						По данным VTT Finland и Университета Аалто, около пяти процентов всех запасных частей могут храниться в цифровом виде, а не в дорогих и переполненных хранилищах. И эта трансформация может принести пользу крупным предприятиям: цифровизация, говорят исследователи, также позволяет легко получить доступ, значительную экономию средств и способность настраивать детали.<br>
						«В настоящее время у промышленности есть все возможности для развития бизнеса за счет того, что запасные части являются неотъемлемой частью развития, - говорит руководитель проекта VTT Sini Metsä-Kortelainen. «Технология 3D-печати достигла той стадии, когда возможно качественное производство».<br>
						Metsä-Kortelainen и другие исследователи, работающие над проектом, все больше убеждаются, что 3D-библиотеки для печати могут быть идеальным решением. С цифровыми библиотеками частей нет такой потери ресурсов. «Капитал выпускается для более продуктивного использования, когда акции уменьшаются», - объясняет Мика Салми, руководитель проекта Университета Аалто. «Еще одна важная возможность заключается в сокращении простоев за счет более быстрого производства запасных частей».<br>
						Но хотя пять процентов - это значительная цифра, эта цифра сама по себе не помогает компаниям определить, какие части должны быть оцифрованы и которые должны оставаться в качестве физического запаса. К счастью, финская исследовательская группа оттачивала то, что, по ее мнению, является наиболее подходящим предметом.<br>
						В проекте было установлено, что чрезвычайно старые или редко необходимые детали наиболее подходят для оцифровки и последующей 3D-печати. Это имеет смысл: детали, которые являются более новыми и часто требуемыми, могут также храниться на складе для легкого доступа, поскольку для них будет постоянный спрос; Тем не менее, те, которые редко необходимы, однако, не должны храниться в натуральном складе за счет производителя.<br>
						Даже для старых редко используемых деталей остается большая проблема: превращение деталей, которые были сделаны с использованием традиционных методов изготовления, в 3D-модели для печати. Это означает, что компании должны определить, какие части можно сделать 3D-печатью, а затем перейти к несколько сложному процессу обратной инженерии для цифровой обработки.<br>
						Финские исследователи отмечают, что автомобильная промышленность в настоящее время хорошо выполняет работу по оцифровке старых запасных частей. Исследование не просто защищает превращение старых продуктов в 3D-печатные. Он также предлагает модернизировать некоторые запасные части путем включения идентификаторов или датчиков, дополнений, которые могут облегчить простой мониторинг машин и оборудования, а также аутентификацию определенных частей.<br>
						Одним из особенно полезных дополнений такого рода является «датчик износа», который может предупредить предприятия, если запасная часть изношена и нуждается в замене. (Футуристические версии могут даже вызвать автоматическое изготовление замененной 3D-печатной части).<br>
