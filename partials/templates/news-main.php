						Компания с «Fives Group S.A.»  создали совместное предприятие под названием «AddUp» для разработки линейки металлических 3D-принтеров, и они будут использовать 3D-печатные пресс-формы для улучшения характеристик шин «Michelin». Роджет признает что реальные временные рамки для реализации 3D-печати шин могут составить от 10 до 15 лет, но надеется, что новые технологии могут изменить это.<br>
