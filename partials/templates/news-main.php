						<h3 class="title">Следующее поколение теплообменников</h3>

						<p class="text">Несмотря на сумасшедшие темпы технологического развития, мы по-прежнему во многом зависим от нескольких основных технологий, которые практически не изменились за последние несколько десятилетий.<br> 
						Хотим обратить внимание на теплообменник, который входит в основу каждого охлаждающего устройства от холодильника до канализационных очистных установок и электростанций.<br>  
						Система, которая всегда была слишком дорогостоящей, чтобы улучшить. Но исследователи из Университета штата Мэриленд использовали 3D-печать металлом, чтобы сделать устройство более дешевым, штучным и более эффективным теплообменником нового поколения. <br> 
						<br> 
						Это огромный прорыв, так как теплообменники играют жизненно важную роль в реалиях современного мира. Почти каждый нагрев или охлаждение системы зависит от теплообменников, которые в буквальном смысле слова обмениваются теплом от одного источника к другому. Это миллиардная промышленность, касающаяся каждой отрасли, которую можно себе представить. И означает, что более эффективная альтернатива будет иметь огромное влияние. Но, несмотря на его широкое применение, конструкция остается практически неизменной на протяжении многих десятилетий, предыдущие попытки внедрения инноваций увенчались неудачей из-за высокой стоимости. <br> 
						<br> 
						Именно здесь и сейчас 3D-печать приходит на помощь, чтобы не погрязнуть в дорогостоящих и долгосрочных разработках, в отличие от традиционного литья.<br> 
						Университет штата Мэриленд, при поддержке от компании 3D Systems и Департамента США технологий Управления строительной энергетики, обратился к технологии металлической 3D-печати. 3D-технологии 3D Systems позволили им, в двух словах, разработать более эффективную, нестандартную форму. С технической точки зрения это уникальный миниатюрный теплообменник, напечатанный в единую твердую деталь. 
						Более того, 3D-печать значительно ускорило развитие. <br> 
						<br> 
						Если традиционные теплообменные прототипы занимали несколько месяцев разработки, то распечатанный на 3D-принтере теплообменник был завершен всего за несколько недель, потому что все изменения и экспериментальные образцы были произведены гораздо быстрее. Результат весит на 20% меньше, чем его предшественник, на 20% эффективнее и производится гораздо быстрее, чем существующие теплообменники. Благодаря твердой форме он гораздо более устойчив к давлению или повреждениям, обладает более длительным сроком хранения, улучшена производительность, более эффективное нагревание или охлаждение. Кроме того колоссальная экономия расходных материалов. <br> 
