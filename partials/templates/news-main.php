						<h3 class="title">Тканевые 3D-структуры от «Nervous System»</h3>

						<p class="text">В штате Массачусетс дизайн-студия «Nervous System» использовала технологию 3D-печати для изучения возможности создания самоформирующихся структур путем печати на растянутой ткани. Основная идея проекта заключалась в том, чтобы изготовить куски тканей в конкретные 3D-формы, напечатав на них различные элементы из пластика, в момент пока они были растянуты. Эти структуры локально препятствуют сжатию поверхности, тем самым задаёт желаемую форму.<br>
						«Nervous System» работает на пересечении науки, искусства и технологий. Она была основана еще в 2007 году, выпускниками Массачусетского технологического института Джессикой Розенкранц и Джесси Луи-Розенбергом. Студия создает уникальные и доступные продукты, используя новый процесс, который объединяет в себе компьютерное моделирование естественных явлений, и цифровые технологии изготовления, такие как 3D-печать, для их реализации.<br>
						Проект «самоформирующиеся структуры ткани» был выполнен в большей степени сотрудником «Nervous System» Гейбом Филдс. Его проект схож с проектом студии «Floraform», который в цифровой форме разработал набор замечательных цветочных структур. Кроме того, идея проектирования 3D-печатного объекта таким образом, что он будет преобразован в желаемую форму на заключительном этапе после печати, путем «программирования» этого окончательного преобразования в структуру, является основным принципом того, что сейчас называется 4D-печать.<br>
						Эксперимент начался с печати узора толщиной 0,3 мм поверх натянутой сверхгибкой ткани, которая была вытянута на печатном слое. После того как растянутая ткань освобождается от напряжения, области, с элементами 3D-печати, не смогут сжаться. За счёт этого, ткань искривляется в трех измерениях, чтобы найти наиболее стабильную форму. Трансформация является результатом эластичности ткани, противопоставленной жесткости пластика, пока противоположные силы не достигнут равновесия.<br>
						Система использовала алгоритм «Boundary-First Flattening», который берет цифровую трехмерную модель и выравнивает ее в два измерения. Это делается путем сокращения некоторых областей и расширения других, эффективно выравнивая и разворачивая 3D-модели. Затем 3D- и 2D-версии модели были загружены в программу «openFrameworks», которая использовалась для вычисления силы сжатия, испытываемого каждой частью модели. Проект «Nervous System» отступил от 2D-формы, изменив сжатие в каждой области, с целью найти форму, которую нужно было распечатать, добившись желаемого окончательного преобразования обратно в исходную 3D-модель. Программа «openFrameworks» создает неравномерный гексагональный макет, который может эффективно отобразить эту модель. <br>
						Команда смогла создать впечатляющую форму человеческого лица с использованием этой технологии. Они напечатали её на машине «Ultimaker 2 FDM». Также экспериментировали с созданием некоторых других форм, как например яйца, седловую форму и форму медузы с длинными усиками. 
                        Работа основывается на некоторых предыдущих проектах, которые исследовали печать на предварительно растянутой ткани. К ним относятся проекты «MIT Self Assembly Lab»: «Programmable Materials» и «Active Shoes». Это предполагает перспективное будущее для 3D-печати, поскольку растущая доступность технологии означает, что все больше и больше дизайнеров смогут исследовать ее творчески.<br>
