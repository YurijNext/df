						<h3 class="title">3D-печать позволяет прикоснуться к артефактам древности</h3>

						<p class="text">Ранее мы сообщали об использовании 3D-технологий в секторе наследия. Это позволило культурным учреждениям точно копировать свои коллекции, как, например, в музеи Вирджинии отсканировали и напечатал некоторые из его артефактов, чтобы дать посетителям осязательный опыт восприятия данных элементов культуры. Разрушенная войной Сирия также увидела свою долю в проектах, связанных с 3D-печатью, направленных на восстановление древних памятников, разрушенных ИСИС. Теперь Google объединился с некоммерческой организацией «CyArk» для запуска Open Heritage, которая будет использовать 3D-технологии для улучшения доступа к другим археологическим чудесам мира.<br>
						В рамках проекта «Open Heritage» отдел искусств и культуры Google захватил подробные изображения 26 известных археологических объектов, включая Помпеи в Италии и Сирийский дворец Аль-Азем. Многим из этих объектов угрожает разрушение, будь то война или стихийные бедствия, такие как землетрясения. Эти изображения и 3D-модели будут доступны бесплатно для пользователей в сети и также загружаемы через приложение iOS или Android. Конечный продукт использования данного материала будет зависеть только от фантазии пользователя.<br>
						«Тайна того, что полезного люди смогут извлечь из этих данных, является волнительным вопросом», - говорит Джон Ристевски, председатель и главный исполнительный директор «CyArk». «Одна из захватывающих граней - это виртуальная и дополненная реальность, и мы рады видеть, какой опыт из данных о наследии могут получать люди, - от погружения в виртуальные туры до пользования разнообразной контекстной информацией, пока вы находитесь на сайте, - и всё это берёт начало с точнейших карт тех мест».<br>
						Для захвата изображений проект «Open Heritage» будет использовать передовую технологию лазерного сканирования LiDAR от «CyArk». CyArk основал Бен Кацира из Мосула, в Ираке. Он принял решение действовать, когда увидел кадры действия членов движения «Талибан», уничтожающих древние буддийские статуи в Бамиане, Афганистан, которые были построены полторы тысячи лет назад. На тот момент он уже участвовал в разработке технологии LiDAR и понял, что ее можно использовать таким образом, чтобы помочь сохранить одни из величайших достижений человечества.<br>
						«Изобретение Бена предназначалось для сложных промышленных сред, таких как нефтеперерабатывающие заводы и подводные лодки, - вещи, которые не так легко захватывались и описывались традиционными методами исследования», - говорит Ристевски. «Органичные формы многих объектов наследия также трудно моделировать, поэтому применение LiDAR оказалось отличным вариантом и в сочетании с современной фотограмметрией может дать нам как точную геометрию, так и текстуры, которые полезны для множества приложений».<br>
						LiDAR быстро создает детальную текстурированную картину среды, известной как «облако точек», запуская лазер на поверхности и измеряя время, необходимое для возврата лазера.<br>
