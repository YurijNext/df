						<h3 class="title">3D-печать, вакуум, лёд и новые грани микро нано обработки</h3>

						<p class="text">В вакуумной среде, при температуре - 130 градусов, водяной пар может конденсироваться в слой супер гладкого тонкого льда. Ученые использовали это для замены материала фоторезиста при традиционном воздействии электронного луча, создавая несколько трехмерных металлических структур в микро - нано масштабе: пирамиды, грибы и мосты. Ожидается, что эта новая и простая электронно-лучевая литография, использующая этот метод защиты ото льда (iEBL или Ice EBL), продемонстрирует потенциал для трехмерной микро нано обработки.<br>
						25 июня 2018 года исследователи из Государственной лаборатории современных оптических инструментов Университета «Чжэцзян» во главе с профессором Цю Вэй опубликовали статью под названием «Трехмерная литография электронного пучка с использованием водяного льда» в журнале «Nano Letters», представляющую Метод 3D-нанообработки на основе электронно-лучевой литографии с использованием ледяных резистов (iEBL). Соавторами этого труда был Юй Хун, докторант в Университете «Чжэцзян», а также профессор Чжао Дин и Цю Ю, профессора оптической инженерии в университете «West Lake».<br>
						iEBL - это разработка стандартной технологии электронно-лучевой литографии (EBL), которая используется для проектирования устройств, систем и функциональных материалов в нано масштабах.<br>
						EBL - это практика сканирования сфокусированного пучка электронов для создания фигур на поверхности, покрытой электронно-чувствительной пленкой, называемой резистом (экспозиция). Воздействие электронного пучка изменяет растворимость резиста, что позволяет выборочно удалять либо открытые, либо неокрашенные области резиста путем погружения его в проявитель. Основным преимуществом электронно-лучевой литографии является то, что она может создавать пользовательские шаблоны (прямая запись) с разрешением до 10 нм.<br>
						Однако в реальных ситуациях это сложно. Небольшая вибрация инструмента, интерференция внешних магнитных полей и опыт оператора повлияют на конечный результат этой формы прямой записи.<br>
						В настоящее время точность воздействия электронного пучка составляет около 60-80 нм, что эквивалентно одной тысячной части человеческого волоса.<br>
						Учитывая растущий спрос на миниатюризацию и усовершенствование микро/нано-устройств, ученые все больше осознают ограничения процесса литографии. Чжао Дин, первый автор статьи и исследователь Университета Чжэцзян, говорит: «Для создания трехмерной структуры требуются очень утомительные и длительные этапы. Повторяя в вакууме и без вакуума, любая пыль в процессе может уничтожить образец».<br>
