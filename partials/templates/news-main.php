						<h3 class="title">Исследователи в Онтарио изготовили на 3D-принтере титановый череп для собаки больной раком</h3>

						<p class="text">Исследователи из университета Онтарио использовали технологию 3D-печати, чтобы заменить большую часть черепа больной раком собаки. Новая процедура лечения рака объявляется важным достижением в области ветеринарной медицины.<br>
						Патчи, девятилетняя такса из Уилламспорт, штат Пенсильвания, имела опухоль мозга размером с апельсин, которая росла через ее череп. Опухоль головного мозга приводила к деформации головы Таксы и была бы фатальной, если бы не лечение, сказал Даниэль Димек, владелец Патчи.<br>
						«Мы назвали ее нашим маленьким единорогом, потому что у нее была эта огромная шишка на голове, и она могла бы убить её, если бы не врачи. Удивительно, что они сделали для моей девочки".<br>
						По словам Димек, шишка, которая росла у Патчи на голове на протяжении многих лет, начала агрессивно расти несколько месяцев назад. Ветеринар Дэниэль, посоветовал ей поехать в Корнельский университет с направлением к доктору Мишель Облак, ветеринарному хирургическому онкологу, работающему в колледже Университета им. Гвельфа в Онтарио, за советом по лечению Патчи.<br>
						Ранние операции для таких собак, как Патчи, состояли бы из удаления опухоли и части черепа, с дальнейшей заменой её титановой сеткой. Доктор Облак сказала, что это была бы неточная, дорогостоящая и продолжительная процедура.<br>
						По ее словам, новая процедура, которая использует 3D-принтер для создания индивидуальной титановой черепной коробки для собаки, намного лучше и качественнее.<br>
						Патчи нуждалась в снятии и замене около 70 процентов ее черепа. Доктор Велак сказал, что ветеринары в Великобритании провели аналогичную операцию, но замена черепа была менее объёмна.<br>
						«Они чувствовали, что она сможет оправиться от этого, - сказала Димек. «И быть частью исследования рака было для меня чем-то значимым - если они могут научиться чему-то на животных, чтобы помочь людям, это очень важно».<br>
						Новый метод начинается с компьютерной томографии опухолей и черепа. Используя несколько различных программ, доктор Облак и ее команда в цифровой форме вырезали опухоль и пораженные болезнью части черепа собаки. Затем они отобразили, где будет помещаться 3D-печатные элементы с отверстиями для винтов, чтобы удерживать их на месте.<br>
						Затем цифровые модели были отправлены в ADEISS, медицинскую 3D-полиграфическую компанию в Лондоне, штат Онтарио, которая изготовила индивидуальную титановую крышку для черепа.<br>
						Облак вместе с несколькими ветеринарами, инженерами-программистами и промышленными инженерами создала «cutting guide», указаниям которого нужно следовать во время операции.<br>
						23 марта такса с раком перенесла четырехчасовую операцию. В течение 30 минут после пробуждения собака отправилась отдыхать в ванную, сказала Облак.<br>
						Благодаря технологии 3D-печати, Патчи теперь свободна от рака. «Ее голова выглядит великолепно, кроме ее кривого уха», - сказал Димек.<br>
						Доктор Облак сказала, что она считает эту процедуру первой в своем роде в Северной Америке, её можно смело оценить как существенный вперёд от ранее известной операции.<br>
						"Мы надеемся на то, что такого рода операции буду более широкодоступны", - сказала Облак. «Все прошло хорошо».<br>
