						<h3 class="title">Летучая лисица с 3D-принтера</h3>

						<p class="text">Мы видели много примеров технологии 3D-печати, используемых для имитации различных вещей из природного мира, в частности уникальных движений и других аспектов поведения животных. Самый последний прорыв - от немецкой электроники, которая напечатала роботическую копию летучей лисицы, которая является родной для частей Азии и Австралии. Изготовленный из 3D-печатных компонентов, а также эластановой ткани (это синтетическая нить, получаемая на основе полиуретановых каучуков), полностью функциональный "BionicFlyingFox" может взлететь по воздуху аналогично созданию матушки природы.<br>
						"Festo" производит широкий спектр электронного оборудования и экспериментирует с аэродинамикой в дизайне робототехники, в частности, на концептах природного мира. Проект "BionicFlyingFox" стал результатом этого исследования, и технология 3D-печати позволила очень точно смоделировать структуру и поверхность гладкого аэродинамического тела крупнейшей в мире летучей мыши.<br>
						Настоящее животное летает, взмахивает крыльями, чтобы продвинуть свое тело, используя пальцы, заправленные под его крыльями, чтобы менять траекторию полета. Мембрана его крыльев - важнейшая часть тела для полета, состоит из тонкого легкого упругого вещества, которое придаёт крыльям многие из тех же характеристик, что и перья для птиц.<br>
						В искусственном прототипе "Festo" инженеры использовали углеродные стержни для создания тела бионической лисы, а также различные 3D-печатные компоненты, которые могли бы эффективно двигаться. Чтобы воспроизвести кожу крыльев летучей мыши, они использовали тонкую эластичную ткань. Эластан обычно используется в одежде, нижнем белье, джинсах, благодаря своей высокой гибкости.<br>
						Готовый "BionicFlyingFox" имеет размах крыльев более 7 футов (228 см) и почти 3 фута длиной (87 см), но он относительно легкий, весит всего 580 грамм. Он имеет около 45 000 точек сварки, что отражает уровень внимания к деталям, связанным с получением дизайна, и необычайную сложность многих природных творений, таких как летучая лисица, технически известная как Pteropus. Машина может выполнить заданную траекторию полета, при этом оператор необходим только для взлета и посадки, для этого используется инфракрасная камера.<br>
						По словам пресс-секретаря "Festo": «Чтобы "BionicFlyingFox" мог перемещаться полуавтономно в определенном пространстве, он общается с системой отслеживания движения. Установка постоянно фиксирует свое положение в пространстве. Человек же лишь запускает и сажает прототип вручную. Автопилот полностью берет на себя управление полетом».<br>
						Он способен висеть вверх дном, парящим в захватывающем погружении, и мягко скользить по земле, не сталкиваясь или не нарушая его окружающую среду. Это пример того, как улучшенный проектный потенциал, относительно дешевые материалы и процессы производства модели посредством 3D-печати, в купе могут позволить максимально проявить инженерам творческий подход, а также продемонстрировать разнообразие природы и потенциал, предлагаемый путем повышения доступности робототехники.<br>
