						<h3 class="title">С помощью нано 3D-принтера учёные распечатали устройство, котрое может предоставить ответы в области рака мозга, исследований Альцгеймера</h3>

						<p class="text">Исследователи из "Istituto Italiano di Tecnologia" в Италии использовали нано 3D-печать для разработки гибридного нанотехнологического устройства, которое могло бы помочь в разработке методов лечения заболеваний головного мозга и опухолей. Устройство имитирует гемато-энцефалический барьер (физиологический барьер между кровеносной системой и центральной нервной системой. Его имеют все позвоночные).<br>
						Это очень полезная мембрана, сформированный эндотелиальными клетками мозга, она позволяет пропускать такие вещи, как вода и некоторые газы, но блокирует определенные нейротоксины и другие вещества.<br>
						Наши ГЭБ естественны, и наша центральная нервная система не будет работать должным образом без них, но исследователи Istituto Italiano di Tecnologia недавно напечатали своего рода искусственный ГЭБ, который можно было бы использовать для развития терапии заболеваний мозга.<br>
						Их гибридное устройство, изготовленное с использованием нано 3D-принтера Nanoscribe, содержит комбинацию искусственных и биологических компонентов и функционирует как микрожидкостное устройство. Это было сделано с использованием комбинации механических и естественных методов.<br>
						Процесс микрофабрикации двухфотонной литографии был использован с помощью машины Nanoscribe для создания нескольких частей модели ГЭБ. Во время этого двухфотонного литографического процесса лазер сканирует жидкий фотополимер и затвердевает материал по слоям, создавая своего рода 3D-печать SLA или DLP, но в субмикронном масштабе.<br>
						Исследователи построили точную модель ГЭБ (в масштабе 1 : 1) из фотополимерной смолы. Его микрожидкостная система, состоящая из 50 параллельных цилиндрических каналов, имитирует микрокапилляры мозга, причем каждая трубчатая структура имеет диаметр всего 10 мкм и поры крошечного диаметра 1 мкм.<br>
						Когда фотополимер был напечатан, эндотелиальные клетки культивировали вокруг пористой микрокапиллярной системы, которая затем, в свою очередь, автономно создавала биологический барьер. Полученное устройство представляет собой своего рода биогибридную систему, очень напоминающую естественный ГЭБ.<br>
						Удивительное устройство можно использовать для лучшего понимания того, как лекарства могут проникать через гемато-энцефалический барьер и нацеливаться на центральную нервную систему и будут использоваться для разработки новых терапевтических стратегий лечения рака мозга, болезни Альцгеймера, рассеянного склероза и других состояний.<br>
						Исследование было задокументировано в статье “A 3D Real-Scale, Biomimetic, and Biohybrid Model of the Blood-Brain Barrier Fabricated through Two-Photon Lithography”, опубликованная в Small.<br>
