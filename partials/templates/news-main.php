						<p class="text">Noumena, инновационная группа инженеров, дизайнеров и архитекторов из Барселоны, исследующая, как искусственный интеллект и роботы могут развиваться и выживать в нашем мире через (частично изготовленную на 3D-принтере) установку под названием «Роботизированные среды обитания».<br>
