						<h3 class="title">3D-печать позволила серферам вывести мастерство на высший уровень</h3>

						<p class="text">Баланс может быть единственной вещью в вашем сознании, когда вы стоите на доске для серфинга в первый раз, но опытные серферы из Университета Вуллонгонга (UOW) всё думают об оборудовании. О тех «настройках», которые можно изменить в материале и дизайне доски, с целью оптимизации, что поможет ловить дополнительные волны. Именно поэтому два года назад исследователи из (UOW) обратились к 3D-печати, чтобы быстро спрототипировать и протестировать многочисленные конструкции досок для серфинга с целью облегчить катание на волнах.<br>
						Это исследование продолжилось. Команда (UOW) из шести серферов и трех исследователей совершила недавнюю поездку на отдаленные острова Ментавай, расположенные за пределами Западной Суматры в Индонезии, чтобы исследовать новые формы досок для серфинга, спроектированные и изготовленные на австралийской национальной фабрике «Australian National Fabrication Facility» при (UOW).<br>
						Эти 3D-печатные доски фактически являются частью программы (UOW) «Global Challenges», целью которой является использование недавно разработанных технологий для изменения жизни во всем мире. Проект возглавляет профессор Марк, который ранее работал над различными проектами 3D-печати, такими как 4D-печатный клапан.<br>
						Как объяснил профессор, их главная цель заключалась в том, чтобы прорваться через общепринятые представления о дизайне и производстве досок для и разработать новые формы, размеры и материалы, которые более эффективны и специально адаптированы к индивидуальному пользователю и волнам, которые этому пользователю предпочтительны.<br>
						Серферы сильно полагаются на две вещи, чтобы ездить на волнах; волне и их доске, сказал профессор. «Существует много мелочей в простом плавнике доски, вы должны рассмотреть основание плавника, глубину, грабли (или развертку), кант, нос доски и её гибкость. Не стоит забывать количество и расположение самих плавников на доске для серфинга».<br>
						«Нет такого понятия, как простой плавник для серфинга. Команда посмотрела на разные материалы, которые могут сделать плавник сильнее, легче и его способность гибнуть».<br>
						Исследователи экспериментировали с 3D-печатными плавниками в течение трех лет.<br>
						«Трехмерная печать позволяет нам печатать практически всё, что мы можем себе представить, в том числе плавники для серфинга», - сказал д-р Стивен Бейрн из Австралийского национального завода при (UOW). «Наша команда начала моделировать плавники на компьютере, а затем мы использовали программу, чтобы рассчитать гидродинамические характеристики, чтобы увидеть, как плавник мог бы работать в воде. Последний этап состоял в том, чтобы выбрать наиболее подходящие материалы для печати прототипа».<br>
						Однако самой сложной задачей было найти последовательную океанскую волну, чтобы проверить характеристики каждого плавника в реальных условиях. После досконального поиска идеального места для проведения испытаний они выбрали цепь островов у западного побережья Суматры в Индонезии из-за множества разрывов прибоя и надежных волн. Команда выбрала особую левостороннюю волну под названием «Macaronis».<br>
						«Macaronis» - уникальное место для серфинга, потому что волны всегда ломаются на рифе в одном и том же месте», - пояснил профессор. «Волны также перекатываются на большие расстояния, и серферы могут получить максимум простора, что идеально подходит для сбора данных».<br>
						Команда поставила задачу поймать как можно больше волн разного типа и выполнять как можно больше поворотов на каждой из них, протестировав все вариации плавников для серфинга.<br>
						В рамках проекта они также полностью укомплектовали доски для серфинга датчиками и устройствами GPS-слежения для сбора данных с каждой волны во время всех поворотов и с учётом всех воздушных потоков. Такой подход позволил им внимательно проследить за выступлениями серферов с различным уровнем мастерства, эти данные пошли в разработку новых проектов.<br>
						«Серферы также проставили оценки по шкале эффективности каждому набору плавников, сразу же после того, как они завершили катание», - объяснил профессор Марк. «Затем информация используется для сравнения разных наборов плавников».<br>
						Профессор Джулия Стил, директор исследовательской лаборатории биомеханики (UOW), собрала данные о рейтинге наборов плавников, с целью защитить исследование от предвзятости.<br>
						«Серферы всегда были рады пойти кататься, но им не разрешалось смотреть под их доски», - сказала профессор Стил. «Чтобы упростить цель, мы изменили для них плавники, и всегда называли их закодированными именами».<br>
						Исследователи отследили и подсчитали, что их серферы оседлали более 450 волн и выполнили более 1700 оборотов во всех типах погодных условий в течение шести дней, при восьми часовом рабочем дне. Они протестировали три разных набора плавников, и сравнили их характеристики производительности с двумя самыми популярными производителями плавников.<br>
						«Предварительный анализ данных о рейтинге показал, что серферы в основном оценили один и тот же набор, - сказал профессор Стил. - Мы были удивлены, что было такое сильное предпочтение для этого одного набора, учитывая, что у каждого из шести серферов были очень разные стили катания».<br>
						В плавнике «Crinkle Cut» имеется ряд канавок на одной стороне плавника. «Подумайте о хрустящей картофельной картофеле, отсюда и название «crinkle cut», - рассказывает профессор Марк. «Причина, по которой эта форма плавника работает так хорошо, заключается в том, что контуры улучшают то, как вода течет мимо нее. Эти контуры в конечном итоге дают серферу большую скорость".<br>
