						<p class="text">«Michelin» хотят интегрировать древесину в ваши шины, и компания полагает, что деревянные шины станут реальностью менее чем за два года.<br>
						«Michelin» объявила о реализации своих амбициях в мае 2048 года, проект включает в себя план производства полностью перерабатываемых шин, при использовании 80% устойчивых материалов.<br>
						Теперь этот проект получил дополнительную огласку французским производитель шин «Motoring» в интервью, посвящённом тому, что «Michelin» исследуют использование древесины в шинах.<br>
						Сегодня «Michelin» разрабатывают инновационные решения, чтобы интегрировать все больше и больше вторичных и возобновляемых материалов в свои шины. Переход к древесным ингредиентам приносит много преимуществ. «Деревья растут повсюду, поэтому вы перераспределяете возможность для каждого иметь местный источник, и они являются возобновляемыми», - сказал Роджет.<br>
						Деревянные шины по-прежнему будут иметь большинство компонентов, используемых сегодня, но эластомеры из древесной щепы заменят содержание масла в шинах.<br>
						«У нас есть проект, основанный на древесной стружке. Мы будем использовать отходы лесной промышленности для создания эластомеров, которые уйдут в шины », - добавил Роджет. «Мы считаем, что это хорошее решение для будущего».<br>
