						Растровые оболочки роботов, которые состоят из нескольких трехмерных печатных разделов, выполняют множество функций. Во-первых, внешний панцирь защищает электронику внутри робота, а во-вторых, он действует как «субстрат для роста бактерий», который эффективно позволяет роботам расти.<br>
