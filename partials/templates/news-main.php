						<h3 class="title">Техника "оригами" TU DELFT в создании 3D-форм решетчатых структур. Новый конкурент для 3D-печати</h3>

						<p class="text">В различных решетчатых структурах, напечатанных 3D-принтерами есть всевозможные удивительные свойства, и они все чаще используются в производстве заказных медицинских имплантатов, легких промышленных деталей и т. д.<br>
						Теперь группа исследователей из TU Delft в Нидерландах разработала новый метод создания трехмерных структур решетки, который опирается на сложные методы складывания, а не на 3D-печать. Команда говорит, что ее инновационный метод может предложить трехмерные решетчатые структуры гораздо большей функциональности, чем их сплошные распечатанные копии. 
                        Исследовательская группа, возглавляемая профессором Амиром Задпуром, говорит, что новый метод создания трехмерных структур решетки был вдохновлен оригами, японским искусством складывания бумаги в сложные формы.<br>
					    Однако, в отличие от традиционных оригами, сложенные трехмерные структуры начинаются как плоские фигуры с использованием усовершенствованной технологии нанолитографии с электронным лучом и впоследствии могут быть сложены в 3D-модели со сложными внутренними формами, аналогичными распечатанным решеткам, но, по-видимому, с более легким доступом к внутренние поверхности.<br>
					    По сообщениям, такой уровень доступа обеспечивает большую функциональность для решеток, которые могут быть спроектированы для выполнения определенных функций и действовать как метаматериалы. Сложенные 3D-структуры могут иметь приложения для создания гибкой электроники, создания новых метаматериалов и разработки медицинских имплантатов со свойствами регенерации тканей.<br>
					    «... Наш доступ к внутренним поверхностям решетчатых 3D-моделей очень ограничен», - сказал Задпур. «Таким образом, объединение орнаментов свободной формы с решетчатыми структурами казалось невозможным. Но, вдохновленный японским искусством бумажной складчатости (оригами), мы нашли способ, который позволяет использовать эту комбинацию».<br>
					    «Мы предложили необычный подход «складывания» структур решетки из первоначально плоских состояний. Такой подход дает нам полный доступ ко всей поверхности того, что в конечном итоге станет нашей структурой решетки. Тогда мы могли бы использовать имеющиеся в настоящее время методы для обработки поверхности ».<br>
					    Задпур добавляет, что исследовательская группа разделила свои решетчатые структуры на три разные категории, каждая из которых имеет четкую стратегию складывания. В отличие от методов 4D-печати, некоторые из сложенных структур решетки объединяют «самосгибающиеся механизмы», которые позволяют плоскому узорчатому материалу автоматически складываться в свою решетчатую структуру при воздействии некоторого элемента, например, изменения температуры.<br>
					    Исследование также показывает, как «3D-структуры свободной формы могут применяться на поверхности плоских листов с разрешением в несколько нанометров». Данная работа была недавно опубликована в журнале Science Advances.</p>
