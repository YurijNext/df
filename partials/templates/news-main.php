						Плоды совместных трудов четырех компаний можно увидеть в их напечатанном на 3D принтере шедевре. Концепция заключается в легкости адаптации и гибкости изготовлении с целью удовлетворить целый ряд вариантов дизайна автомобилей, особенно в отношении различных приводов. Гибридный кузов автомобиля объединяет в себе совокупность изготовленных кузовных узлов, которые произведены методом 3D-печати, с интеллектуально обрабатываемыми профилями, которые могут быть индивидуально скорректированы в соответствии с необходимыми требованиями производства без дополнительной оснастки, оборудования или стартовых затрат. Отдельные части кузова соединяются с помощью стальных профилей, которые могут быть легко адаптированы за счет различных толщин и геометрических форм. <br> 
