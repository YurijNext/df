						<h3 class="title">Сошедшие с 3D-принтера формы используют для хранения информация</h3>

						<p class="text">Исследования «New University of Nottingham» доказывают, что передовые материалы, содержащие молекулы, которые в свою очередь меняют состояния в ответ на стимулы, такие как свет, могут быть изготовлены с использованием трехмерной печати.<br>
