						<h3 class="title">3D-печать выступила мостом между концепцией перерождения Нери Оксмана и её реализацией посредством масок смерти</h3>

						<p class="text">Ранее мы сообщали о применении 3D-печати «MIT Mediated Matter Group» для создания будоражащего набора «масок смерти». Последний сборник из пяти масок, являющийся частью дизайнерской выставки «Новая древняя коллекция» при кураторе Наоми Каемпфер, был третьим и окончательным в трилогии «Vespers». Эти последние пять масок напечатанные из биоактивных материалов, являются выражением концепции перерождения и предназначены для её изучения и пространственного восприятия. «Mediated Matter Group» возглавляется американо-израильским дизайнером и профессором Нери Оксманом, наряду с Кристофом Бадером, Рэйчел Соо Ху Смит, Домиником Колбом, Сунандой Шармой, Жоао Коста и Джеймсом Уивером.<br>
						Конструкции для масок были созданы с использованием расширенного пользовательского программного обеспечения, которое использовалось для моделирования сложных форм с высоким разрешением. Эти 3D-модели были напечатаны многослойным 3D-принтером «Stratasys Objet500 Connex3» с использованием биоактивных материалов.<br>
						Проект «Vespers» стартовал в 2016 году с первыми пятью масками смерти, основанных на древних масках смерти, которые были сформованы из глины или других материалов. Первый набор, который будет показан, на самом деле является второй частью трилогии, известной как «Present, The Digital World». Эти пять масок были созданы для передачи идей метаморфоз, а их цвета и формы были основаны на идеях первой части трилогии, которая была раскрыта позже.<br>
						«Использование алгоритмов пространственного картографирования в купе с цветовым кодированием и и усеченной геометрией в первой серии преобразуется в цветные внутренние линии в прозрачных плавно изогнутых элементах  во второй», - поясняет команда.<br>
						Второй набор назывался «Прошлое» и «Природный мир», его концепция заключалась в том, что последнее дыхание пяти мучеников было заперто внутри маски, в которой они были захоронены. Физический поток воздуха был представлен закрученными узорами в материале, выполненными с использованием разных минералов в 3D-печатной структуре.<br>
						Для третьей и заключительной части трилогии «Будущее: биологический мир», маски должны функционировать как биологические урны. Для этого они заселены различными живыми микроорганизмами. Эти микробы синтетически сконструированы командой Оксмана для производства цветных пигментов, а также видов полезных химических веществ, которые часто используются для роста и сопротивляемости человеческого организма, таких как витамины, антитела или противомикробные препараты. «Mediated Matter Group» хотела предложить возможность того, что будущие биоинженерные костюмы, устройства и интерфейсы могут быть персонализированы в соответствии с химическим и генетическим составом человека, а также его физической формой.<br>
						Он способен висеть вверх дном, парящим в захватывающем погружении, и мягко скользить по земле, не сталкиваясь или не нарушая его окружающую среду. Это пример того, как улучшенный проектный потенциал, относительно дешевые материалы и процессы производства модели посредством 3D-печати, в купе могут позволить максимально проявить инженерам творческий подход, а также продемонстрировать разнообразие природы и потенциал, предлагаемый путем повышения доступности робототехники.<br>
						По словам команды, «эти маски кажутся парадоксально самыми живыми из трех серий: они буквально реорганизуют жизнь, направляя живые микроорганизмы через крошечные пространственные особенности в артефактах мертвых. Их микроорганизмы распределяются в соответствии с пространственной логикой, обеспечиваемой второй серией ... Проект указывает на ближайшее будущее, в котором носимые интерфейсы и «кожи» настраиваются не только в соответствии с конкретной формой, но и с конкретным материалом, химическим и даже генетическим составом, адаптируются к износу как относительно тела, так и относительно окружающей среды».<br>
						Команда допускает, что изученное проявление заданных в последней серии масок параметров, может привести к медицинским интерфейсам, которые выпускают антибиотики в соответствии с генетическими характеристиками человека, а также интеллектуальную аппаратную часть, способную обнаруживать загрязнение и архитектурные поверхности, которые отвечают и адаптируются к экологическим сигналам.<br>
