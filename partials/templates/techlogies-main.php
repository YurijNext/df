									<h3 class="title">MJP технология</h3>
									
									<p class="text">Технологи 3D-печати -Multi Jet Printing (MJP) -  метод многоструйного моделирования, которая очень напоминает обычную струйную печать: основной материал и материал поддержки подаются через сопла малого диаметра, расположенные рядами на печатающей головке. После этого слой засвечивает ультрафиолетовая лампа для того, чтобы произошел процесс полимеризации (застывания). Таким образом наносится слой за слоем и формируется объект. Схожие технологии PolyJet и DODJet. <br> 
									<br>
									Материалы:<br> 
									Жидкий полимер - основной материал.<br> 
									Воск – материал поддержки.<br> 
									<br>
									Толщина слоя от 0,016 мм до 0,032 мм. <br> 
									На наш взгляд, самая совершенная и продвинутая технология для создания прототипов с высокой детализацией. Изделия, напечатанные по технологии MJP, превосходного качества с гладкой поверхностью, и идеально подходят в качестве мастер-моделей для последующего создания силиконовых форм под литье.</p>
