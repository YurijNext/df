									<br>
									Материалы:<br> 
									ПММА (PMMA) - порошок модифицированного полиметилметакрилата, который востребован для изготовления мастер-моделей, применяющихся при литье из стали, чугуна и прочих сплавов. Также активно используется для создания архитектурных макетов и разного рода художественных инсталляций.<br> 
									<br>
									Такое широкое распространение технологии 3D-печати BJ из материала ПММА получила по ряду немаловажных причин:<br> 
									1. Стоимость материала достаточно низкая.<br> 
									2. Габариты готовых изделий могут превышать 50 см.<br> 
									3. При построении не используется поддерживающий материал. <br> 
									<br>
									Толщина слоя при печати на 3D-принтере составляет 150 мкм (позиционирование по осям x и y – 100 мкм), при этом поверхность получается шероховатой. <br> 
									Готовое изделие дополнительно пропитывают специальными растворами в зависимости от целей использования распечатки. </p>
