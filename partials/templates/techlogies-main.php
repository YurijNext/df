									<h3 class="title">SLS технология</h3>
									
									<p class="text">Технология 3D-печати – Selective Laser Sintering (SLS), основана на последовательном спекании слоев, состоящих из полиамида, посредством лазерного луча.<br> 
									<br>
									Полиамид - слегка шероховатый материал, обладает хорошей гибкостью и прочностью, не токсичен. Данная технология 3D-печати идеально подходит для создания промышленных деталей, обладающий сложной геометрией и высокой прочностью.<br> 
									<br>
									Принцип работы SLS-технологии принципиально отличается от FDM подачей материалов. Если при многослойном наплавлении и есть необходимость в поддержке, которая удаляется на этапе постобработки, то при создании 3D-модели с помощью SLS такая надобность отпадает из-за использования порошкового материала, который безотходен, что существенно упрощает работу, а так же повышает точность исполнения и прочность. Эта технология лучше всего подходит для 3D-печати, воплощающей в жизнь сложную инженерную мысль: детали двигателей, высокопрочные элементы промышленных механизмов и корпусов.</p>
