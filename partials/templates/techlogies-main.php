									Принцип работы заключается в многократном нанесении слоя металлического порошка заданной толщины с последующим выборочным плавлением лазером, в соответствии с геометрией сечения изготавливаемого слоя детали. Каждый слой разделяется на множество мельчайших блоков, которые обрабатываются в случайном порядке и разных направлениях, что в свою очередь многократно снижает внутреннее напряжение, а так же позволяет изготовить монолитные крупногабаритные детали.<br> 
									<br> 
									Материалы:<br> 
									Нержавеющая сталь,<br> 
