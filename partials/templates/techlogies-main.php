									<h3 class="title">SLA технология</h3>
									
									<p class="text">Технология StereoLithography Apparatus (SLA) или cтереолитография придает моделям определенно новые свойства, поскольку в основе технологии лежит принцип полимеризации, то есть затвердевания жидкого фотополимера под действием облучения лазером.<br> 
									<br>
									Главным преимуществом этой 3D-технологии является максимальная точность контуров модели, которая вычерчивается 3D-принтером последовательными слоями толщиной от 0,05мм до 0,15мм. Каждая модель, созданная с применением SLA-технологии, являет собой готовое изделие высокого качества, что уже нашло применение в медицине, дизайне и искусстве. <br>
									<br>
									Построение прототипа происходит следующим образом: сетчатая платформа погружается в жидкий фотополимер. Лазер обрабатывает контуры, согласно данным 3D-модели, которые затвердевают под действием излучения. Когда прототип возведен полностью, он погружается в специальную жидкость, чтобы полностью очиститься от лишних элементов. В завершении, полученное изделие полностью облучается светом, чтобы зафиксировать конечную форму.<br>
									Таким образом, SLA-технология позволяет производить высокоточную печать изделий с тонкими гранями и полостями, что без сомнения открывает совершенно новые возможности материализации Ваших идей.</p>
