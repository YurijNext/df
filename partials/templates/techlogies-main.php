									<h3 class="title">FDM технология</h3>
									
									<p class="text">Самая популярная технология 3D-печати на данный момент – Fused Deposition Modeling (FDM) или технология многослойного наплавления. <br>
									<br>
									Используемые материалы: <br>
									ABS – нетоксичный пластик, ударопрочный и эластичный. Модели, сделанные из ABS-пластика, долговечны. Широко применяется для корпусов электроники и в автомобилестроении. Обладает усадкой после 3D-печати. Температура, при которой деформируется - 100 градусов. <br>
									PLA – экологический материал, производится из кукурузы и сахарного тростника. Очень прост в использовании. Практически не обладает усадкой, благодаря чему не деформируется. Температура размягчения - 70 градусов. <br>
									Нейлон - очень прочный материал. Изделия из такого материала практически не поддаются механическим повреждениям. Обладает высокой усыхаемостью, поэтому чаще всего требуется изготовление нескольких образцов перед финальным производством.<br> 
									FLEX (резина) - эластичный и гибкий материал, высокая усыхаемость. В основном используется для создания прокладок и кожухов.<br> 
									<br>
									Принцип работы: <br>
									Цифровая модель загружается в ПО 3D-принтера, где происходит расчет необходимого количества материала и времени для построения образца. Программа разбивает весь процесс печати на последовательные горизонтальные слои, после чего запускается печать. На головку принтера, так называемый экструдер, разогретый до температуры плавления материала, подается специальная нить пластика, и, следуя данным 3D-модели, слои последовательно наносятся на подвижную платформу. Так, слой за слоем, вырастает прототип. <br>
									<br>
									Полученные технологией FDM детали отличаются прочностью и упругостью, а выбор материала отвечает за такие характеристики как термостойкость, износоустойчивость, гибкость. Прототипы, напечатанные многослойным наплавлением, получаются одноцветными – по умолчанию белыми – однако, на данный момент существует выбор цвета из почти десятка опций. Еще одно из преимуществ данной технологии – широкие возможности постобработки детали после печати: их можно полировать, покрывать лаком или краской, сверлить, склеивать или же сплавлять между собой, получая таким образом крупногабаритные модели.</p>
